<p>
Bipolaire stoornis ookal genoemd manischdepressieve stoornis genoemd. Is een stoornis die wordt gemerkt door wisselende stemmingen (je kan dus van boos snel naar blij gaan bijvoorbeeld). Het komt niet heel vaak voor deze stoornis 2-4% van de bevolking. Je kan het soms heel laat gaan merken sommige mensen hebben soms hun episodes jaren niet.    
De stemmingswisselingen kunnen af en toe optreden. 
</p>

<h5>&nbsp;</h5>
<p>Er zijn ook meerdere verschillende soorten bipolaire.</p>
<ul>
  <li><font color="#ff5757"><b>Bipolaire stoornis 1:</b></font> er treedt minimaal een manische of gemengde episode op, maar er kunnen ook hypomane of depressieve perioden optreden.</li>
  <li><font color="#ff5757"><b>Bipolaire stoornis 2:</b></font> er is minimaal een episode van hypomanie en minimaal een depresieve episode. Er is geen manische of gemengde episode opgetreden.</li>
  <li><font color="#ff5757"><b>Cyclothyme stoornis:</b></font> er treedt een reeks hypomane episoden op, onderbroken door episoden van lichtere depressie en uitputting.</li>
</ul> 
